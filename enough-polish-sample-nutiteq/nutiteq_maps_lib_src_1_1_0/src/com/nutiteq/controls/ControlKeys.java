package com.nutiteq.controls;

/**
 * Defines internal action codes used by library. Used by application for keys
 * mapping.
 */
public interface ControlKeys {
  /**
   * 
   */
  int NO_ACTION_KEY = -1;
  /**
   * Code for move map up key
   */
  int MOVE_UP_KEY = 0;
  /**
   * Code for move map down key
   */
  int MOVE_DOWN_KEY = 1;
  /**
   * Code for move map left key
   */
  int MOVE_LEFT_KEY = 2;
  /**
   * Code for move map right key
   */
  int MOVE_RIGHT_KEY = 3;
  /**
   * Code for zoom in key
   */
  int ZOOM_IN_KEY = 4;
  /**
   * Code for zoom out key
   */
  int ZOOM_OUT_KEY = 5;
  /**
   * Select key
   */
  int SELECT_KEY = 6;
}
